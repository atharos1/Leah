void drawChar(int x, int y, char character, int fontColor, int backgroundColor) {

	int aux_x = x;
	int aux_y = y;

	char bitIsPresent;
	char* toDraw = font8x8_basic[character];

	for(int i = 0; i < 8; i++) {
		for(int j = 0; j < 8; j++) {
			bitIsPresent = (1 << j) & toDraw[i];
			if(bitIsPresent)
				drawPixel(aux_x, aux_y, fontColor);
			else
				drawPixel(aux_x, aux_y, backgroundColor);
			aux_x++;
		}
		aux_x = x;
		aux_y++;
	}
}

void invertChar(int x, int y) {
	int aux_x = x;
	int aux_y = y;

	for(int i = 0; i < 8; i++) {
		for(int j = 0; j < 8; j++) {
			invertPixel(aux_x, aux_y);
			aux_x++;
		}
		aux_x = x;
		aux_y++;
	}
}

